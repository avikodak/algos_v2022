#include "v1/common/Includes.h"

class Solution {
public:
	int hammingWeight(uint32_t n) {
		if (n < 0) {
			n = n ^ UINT32_MAX;
			n += 1;
		}
		int setBits = 0;
		while (n) {
			setBits += n % 2;
			n /= 2;
		}
		return setBits;
	}
};
