#include "v1/common/Includes.h"

class Solution {
public:
	string toLowerCase(string s) {
		for (int counter = 0; counter < s.size(); counter++) {
			if (isupper(s[counter])) {
				s[counter] = s[counter] - 'A' + 'a';
			}
		}
		return s;
	}
};
